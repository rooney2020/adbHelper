function createGeneratedDumpsysCommand(service: string): CommandMeta {
  const suffix = service.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "") || "service";

  return {
    id: `dumpsys-generated-${suffix}`,
    title: `adb shell dumpsys ${service}`,
    summary: `查看 ${service} 服务输出。`,
    type: "查看型",
    support: "支持",
    risk: "低",
    raw: `adb shell dumpsys ${service}`,
    prerequisite: "设备在线",
    fallback: "adb shell dumpsys -l"
  };
}

